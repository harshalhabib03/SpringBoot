package com.task1.Integration.model;
import jakarta.persistence.Entity;


public class Employee {
    String name;

    Employee(String name){
        this.name=name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Employee{" +
                "name='" + name + '\'' +
                '}';
    }
}
